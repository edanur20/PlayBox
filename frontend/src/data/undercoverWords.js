// Fonction pour choisir un événement aléatoire basé sur les probabilités
export const getRandomEvent = () => {
  const rand = Math.random();
  let cumulative = 0;

  for (const event of specialEvents) {
    cumulative += event.probability;
    if (rand < cumulative) {
      return event;
    }
  }

  return specialEvents[0]; // Normal par défaut
};

// Fonction pour choisir un rôle spécial aléatoire
export const getRandomRole = () => {
  const rand = Math.random();
  let cumulative = 0;

  for (const role of specialRoles) {
    cumulative += role.probability;
    if (rand < cumulative) {
      return role;
    }
  }

  return specialRoles[0]; // Pas de rôle par défaut
};

