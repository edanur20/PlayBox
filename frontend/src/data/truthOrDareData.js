// Fonction pour obtenir des éléments aléatoires
export const getRandomTruth = (mode) => {
  let truths;
  switch (mode) {
    case 'soft':
      truths = truthsSoft;
      break;
    case 'chaos':
      truths = truthsChaos;
      break;
    default:
      truths = truthsClassic;
  }
  return truths[Math.floor(Math.random() * truths.length)];
};

export const getRandomDare = (mode) => {
  let dares;
  switch (mode) {
    case 'soft':
      dares = daresSoft;
      break;
    case 'chaos':
      dares = daresChaos;
      break;
    default:
      dares = daresClassic;
  }
  return dares[Math.floor(Math.random() * dares.length)];
};

