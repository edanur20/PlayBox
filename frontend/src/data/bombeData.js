export const getRandomTheme = (mode) => {
  let themes;
  switch (mode) {
    case 'spicy':
      themes = themesSpicy;
      break;
    case 'chaos':
      themes = themesChaos;
      break;
    default:
      themes = themesClassique;
  }
  return themes[Math.floor(Math.random() * themes.length)];
};

export const getStats = () => {
  return {
    classic: {
      themes: themesClassique.length,
    },
    spicy: {
      themes: themesSpicy.length,
    },
    chaos: {
      themes: themesChaos.length,
    },
  };
};
