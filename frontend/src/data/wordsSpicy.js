// Fonction pour obtenir tous les mots spicy
export const getAllSpicyWords = () => {
  const allWords = [];
  Object.values(spicyCategories).forEach(category => {
    allWords.push(...category.words);
  });
  return allWords;
};

// Fonction pour générer une grille de 25 mots avec max 1 mot par catégorie
export const generateSpicyGrid = () => {
  const categories = Object.keys(spicyCategories);
  const selectedWords = [];
  const usedCategories = new Set();

  // Mélanger les catégories
  const shuffledCategories = [...categories].sort(() => Math.random() - 0.5);

  // Sélectionner 25 mots de 25 catégories différentes
  for (let i = 0; i < 25 && i < shuffledCategories.length; i++) {
    const categoryKey = shuffledCategories[i];
    const category = spicyCategories[categoryKey];
    const words = category.words;
    const randomWord = words[Math.floor(Math.random() * words.length)];
    selectedWords.push(randomWord);
    usedCategories.add(categoryKey);
  }

  // Si on n'a pas assez de catégories (ne devrait pas arriver avec 25+), compléter
  while (selectedWords.length < 25) {
    const randomCategory = categories[Math.floor(Math.random() * categories.length)];
    const category = spicyCategories[randomCategory];
    const words = category.words;
    const randomWord = words[Math.floor(Math.random() * words.length)];
    if (!selectedWords.includes(randomWord)) {
      selectedWords.push(randomWord);
    }
  }

  return selectedWords.sort(() => Math.random() - 0.5);
};

