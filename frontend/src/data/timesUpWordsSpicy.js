// Générer une liste de mots spicy pour une partie
export const getSpicyGameWords = (count = 30) => {
  const allWords = [];

  // Collecter tous les mots de toutes les catégories
  Object.values(spicyCategories).forEach(category => {
    allWords.push(...category.words);
  });

  // Mélanger
  const shuffled = [...allWords].sort(() => Math.random() - 0.5);

  // Retourner le nombre demandé
  return shuffled.slice(0, count);
};

// Version avec une sélection par catégorie (pour plus de variété)
export const getSpicyGameWordsByCategory = (count = 30) => {
  const categories = Object.values(spicyCategories);
  const wordsPerCategory = Math.ceil(count / categories.length);
  const selectedWords = [];

  // Sélectionner des mots de chaque catégorie
  categories.forEach(category => {
    const shuffledCategoryWords = [...category.words].sort(() => Math.random() - 0.5);
    selectedWords.push(...shuffledCategoryWords.slice(0, wordsPerCategory));
  });

  // Mélanger le tout et retourner le nombre demandé
  return selectedWords.sort(() => Math.random() - 0.5).slice(0, count);
};

